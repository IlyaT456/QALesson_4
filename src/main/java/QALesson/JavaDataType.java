package QALesson;

public class JavaDataType {

    public void javaDataType() {
        // byte 8 бит от -128 до 127
        byte aByte;
        // short 16 бит от -32768 до 32767
        short aShort;
        // int 32 бита от -2147483648 до 2147483647
        int aInt;
        // long 64 бита от от -9223372036854775808 до 9223372036854775807
        long aLong;
        // float 32 бита от 2-149 до (2-2-23)*2127
        float aFloat;
        // double 64 бита от 2-1074 до (2-2-52)*21023
        double aDouble;
        // символ UTF-16 (буквы и цифры)
        char aChar;
    }

}
